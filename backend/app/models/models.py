class Car:
    def __init__(self, vin, year, make, model, listing_price, listing_mileage, dealer_city, dealer_state):
        self.vin = vin
        self.year = year
        self.make = make
        self.model = model
        self.listing_price = listing_price
        self.listing_mileage = listing_mileage
        self.dealer_city = dealer_city
        self.dealer_state = dealer_state 